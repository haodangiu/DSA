package src.Week3;

import java.util.Scanner;

public class Sherlock_and_Array {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        int t = input.nextInt();
        while (t != 0) {
            int n = input.nextInt();
            int[] a = new int[n];
            for (int i = 0; i < n ; i ++)
            {
                a[i] = input.nextInt();
            }


        }


    }

}
